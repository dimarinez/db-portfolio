Template.heineken.onRendered(function() {

$('.go-back').on('click', function() {
  $('body').addClass('heineken-anchor');
});


  $(document).ready(function() {
    $('iframe').attr('src', 'http://metajive.com/work/heineken2'); 
  });

  $('.main-content').css({'height' : '100%'});
  setTimeout( function() {
    $('.preloader').fadeOut();

  }, 3000);
});

